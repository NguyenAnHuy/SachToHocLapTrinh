#---------------------HÀM ĐIỀU KIỆN IF, ELIF VÀ ELSE-------------------

traloi=input("Mai thích anh ở điểm gì?")
if traloi=="Tay anh to":
	print("Sờ ngực cô ấy")
elif traloi=="Mắt anh to":
	print("Nhìn mông cô ấy")
elif traloi=="Chim anh to":
	print("Bế cô ấy lên giường")
else:
	print("Em làm anh đau lòng quá")
